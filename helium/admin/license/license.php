<p>The license under which the E12E13E14TS CMS Helium H2 software is released is the GPL (or later) from the <a href="http://www.fsf.org/">Free Software Foundation</a>. A copy of the license is included with every copy of E12E13E14TS CMS Helium H2, but you can also read the <a title="license" href="http://e12e.net/admin/license/GPL.txt" target="_blank">text of the license</a> here.</p>
<p>Part of this license outlines requirements for derivative works, such as themes. Derivatives of E12E13E14TS CMS Helium H2 code inherit the GPL license. <a href="http://drupal.org/">Drupal</a>, which has the same GPL license as E12E13E14TS CMS Helium H2, has an excellent page on <a href="http://drupal.org/licensing/faq/">licensing as it applies to themes</a>.</p>
<p>There is some legal grey area regarding what is considered a derivative work, but we feel strongly that themes are derivative work and thus inherit the GPL license.</p>
